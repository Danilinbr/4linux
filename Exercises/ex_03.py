#!/usr/bin/python3

#1: Capturar dois números

n1 = int(input('N1: '))
n2 = int(input('N2: '))

#2: realizar a soma dos dois números
soma = n1 + n2

#3: realizar a diferença dos dois números
dif = n1 - n2 

#4: apresentar o resultado conforme solicitado
print('--------------------------------')
print(f'Soma: {n1} + {n2} = {soma}')
print(f'Diferença: {n1} - {n2} = {dif}')


